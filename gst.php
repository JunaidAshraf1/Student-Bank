<h4>6.0 GST Council Meetings</h4><br>

&nbsp &nbsp &nbsp &nbsp GST Council has met sixteen times since its constitution and some important 
decisions taken in the GST Council meetings &nbsp &nbsp &nbsp &nbsp are:-<br>
<ul>
<li>Rules for conduct of business in GST Council;</li>
 <li>Timetable for implementation of GST;</li>
 <li>The threshold limit for exemption from levy of GST would be Rs. 20 lakhs for the States
 except for the Special Category States, as enumerated in Article 279A of the Constitution, 
 for which it will be Rs 10 Lakhs);</li> 
<li> The threshold for availing the Composition scheme 
 would be Rs. 75 lakhs. (Enhanced from Rs 50 lakhs to Rs 75 lakhs at the 16th GST Council 
 Meeting held in New Delhi on 11th June 2017). Service providers and some others would be 
 kept out of the Composition Scheme;</li>
 <li>To compensate States for 5 years for loss of revenue 
 due to implementation of GST, the base year for the revenue of the State would be 2015-16 
 and a fixed growth rate of 14% will be applied to it;</li>
 <li>Approval of the Draft GST Rules on registration, payment, 
 return, refund and invoice, debit/credit Notes with the understanding 
 that minor changes may be permitted with the approval of the Chairperson, if required, based
 on suitable suggestions from the stakeholders or from the Law Department; </li> 
 <li>All entities exempted  from payment of indirect tax under 
any existing tax incentive scheme would pay tax in the GST regime and the decision 
 to continue with any incentive scheme shall be with the concerned State
 or Central government. In case, the State or Central Government decides to continue with any 
 existing exemption/incentive scheme; it will be administered by way of a reimbursement mechanism. </li>
 
<li> Adoption of four slabs tax rate structure of 5%, 12%, 18% and 28%. In addition, there would be a 
 category of exempt goods and further a cess would be levied on certain goods such as luxury cars,
 aerated drinks, pan masala and tobacco products, over and above the rate of 28% for payment of
 compensation to the states.
 </li>
 <li>GST rates on majority of items were approved at the 14th GST Council 
 meeting held at Srinagar on 18th and 19th of May 2017.</li>
 <li>At the 15th GST Council meeting held at
 New Delhi on 3rd June 2017, tax rates on the remaining goods were approved. </li>
 24 states, and 2 Union Territories with Legislatures (Delhi and Puducherry) 
 have already passed their respective State GST Bill in their State Assemblies.</li>
 <li>Issue of cross empowerment and administrative division of 
 taxpayers between the States and Centre has been resolved.</li>
 <li>Rates on 66 items reduced at the 16th 
 GST Council Meeting held in New Delhi on 11th June 2017.</li>
 </ul><br>
 

