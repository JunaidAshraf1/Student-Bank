<h2>GST – The Game Changer</h2><br>
The genesis of the introduction of GST in the country was 
laid down in the historic Budget Speech of 28th February 2006, 
wherein the then Finance Minister laid down 1st April, 2010 as 
the date for the introduction of GST in the country. Thereafter, 
there has been a constant endeavor for the introduction of the GST
 in the country whose culmination has been the introduction of the 
 Constitution (122nd Amendment) Bill in December, 2014.<br><br>

<h4>Why GST?</h4><br>

2.0 A common refrain in the popular discussions is what is 
the need for the introduction of GST? To answer that question, 
it is important to understand the present indirect tax structure 
in our country. Presently the Central Government levies tax on manufacture 
(Central Excise duty), provision of services (Service Tax), interstate 
sale of goods (CST levied by the Centre but collected and appropriated by 
the States) and the State Governments levy tax on retail sales (VAT), 
entry of goods in the State (Entry Tax), Luxury Tax, Purchase Tax, etc. 
It is clearly visible that there are multiplicities of taxes which are 
being levied on the same supply chain.<br><br>

There is cascading of taxes, as taxes levied by the Central Government 
are not available as setoff against the taxes being levied by the State 
governments. Even certain taxes levied by State Governments are not 
allowed as set off for payment of other taxes being levied by them. 
Further, a variety of VAT laws in the country with disparate tax rates 
and dissimilar tax practices, divides the country into separate economic 
spheres. Creation of tariff and non- tariff barriers such as Octroi, 
entry Tax, Check posts etc. hinder the free flow of trade throughout 
the country. Besides that, the large number of taxes creates high compliance 
cost for the taxpayers in the form of number of returns, payments etc.<br><br>

<h4>What is GST?</h4><br>

3.0    All the taxes mentioned earlier are proposed to be subsumed in a 
single tax called the Goods and Services Tax (GST) which will be levied 
on supply of goods or services or both at each stage of supply chain starting 
from manufacture or import and till the last retail level. So basically any tax 
that is presently being levied by the Central or State Government on the supply 
of goods or services is going to be converged into GST.<br><br>

3.1   GST is proposed to be a dual levy where the Central Government will 
levy and collect Central GST (CGST) and the State will levy and collect State 
GST (SGST) on intra-state supply of goods or services. The Centre will also levy 
and collect Integrated GST (IGST) on inter-state supply of goods or services. 
Thus GST is a unifier that is going to integrate various taxes being levied by 
the Centre and the State at present and provide a platform for forging an economic 
union of the country.<br>

3.2   This tax reform will lead to creation of a single national market, 
common tax base and common tax laws for the Centre and States. Another
 very significant feature of GST will be that input tax credit will be 
 available at every stage of supply for the tax paid at the earlier stage 
 of supply. This feature would mitigate cascading or double taxation in a
 major way. This tax reform will be supported by extensive use of Information
 Technology [through Goods and Services Tax Network (GSTN)], which will lead to 
 greater transparency in tax burden, accountability of the tax administrations 
 of the Centre and the States and also improve compliance levels at reduced cost 
 of compliance for taxpayers. Studies indicate that introduction of GST would
 instantly spur economic growth and can potentially lead to additional GDP growth 
 in the range of 1% to 2%.<br><br>

 <h4>Advantages of GST</h4><br>

 <p><h4>4.0 Advantages for the government:</h4></p><br>
<ul>
<li>Will help to create a unified common national market for India, 
giving a boost to foreign investment and “Make in India” campaign; </li>
<li>Will  mitigate cascading of taxes as Input Tax Credit will be available
 across goods and services at every stage of supply; Harmonization of laws, 
 procedures and rates of tax between Centre and States and across States;</li>
<li> Improved environment for compliance as all returns are to be filed online,
 input credits to be verified online, encouraging more paper trail of 
 transactions at each level of supply chain;</li>
<li> Similar uniform SGST and IGST rates will reduce the incentive 
 for evasion by eliminating rate 
 arbitrage between neighbouring States and that between intra and inter-state 
 sales; </li>
 <li>Common procedures for registration of taxpayers, refund of taxes, 
 uniform  formats of tax return, common tax base, common system of classification 
 of goods and services will lend greater certainty to taxation system;</li>
 <li>Greater use of IT will reduce human interface between the taxpayer and the tax 
 administration, which will go a long way in reducing corruption;</li>
 <li>It will boost export and manufacturing activity, generate more employment 
 and thus increase GDP with gainful employment leading to substantive  
 economic growth;</li> 
 <li>Ultimately it will help in poverty eradication by generating
 more employment and more financial resources.</li>
  </ul><br>

<h4><p>4.1 Advantages to Trade and Industry:<p></h4><br>

<ul>
<li>Simpler tax regime with fewer exemptions; Increased ease of doing business; </li>
<li>Reduction in multiplicity of taxes that are at present governing our
 indirect tax system leading to simplification and uniformity;</li>
 <li>Elimination of double taxation on certain sectors like works contract, software, hospitality sector;</li>
<li> Will mitigate cascading of taxes as Input Tax Credit will be available across goods and
 services at every stage of supply;</li>
 <li>Reduction in compliance costs - No multiple record 
 keeping for a variety of taxes - so lesser investment of resources and manpower in maintaining 
 records;</li>
 <li>More efficient neutralization of taxes especially for exports thereby making 
 our products more competitive in the international market and give boost to Indian Exports;</li> 
 <li>Simplified and automated procedures for various processes such as  registration, returns, 
 refunds,  tax payments, etc;</li>
 <li>Average tax burden on supply of goods or services is expected 
 to come down which would lead to more consumption, which in turn means more production 
 thereby helping in the growth of the industries manufacturing in India.</li>
</ul><br>
<h4>4.2 Advantages to Consumers:</h4><br>
<ul>
<li>Final price of goods is expected to be transparent due to seamless flow of 
input tax credit between the manufacturer, retailer and service supplier;</li>
<li>Reduction in prices of commodities and goods in long run due to reduction 
in cascading impact of taxation;</li> 
<li>Relatively large segment of small retailers 
will be either exempted from tax or will suffer very low tax rates under 
a compounding scheme - purchases from such entities will cost less for the 
consumers;</li>
<li>Poverty eradication by generating more employment and more financial resources.</li>
</ul><br>
 

<h4>4.3 Advantages to States:</h4><br>
<ul>
<li>Expansion of the tax base as they will be able to tax the entire supply chain 
from manufacturing to retail;</li>
<li>Power to tax services, which was hitherto with the 
Central Government only, will boost revenue and give States access to the fastest 
growing sector of the economy; </li>
<li>GST being destination based consumption tax will favour
 consuming States; </li>
 <li>Improve the overall investment climate in the country which will
 naturally benefit the development in the States;</li> 
 <li>Largely uniform SGST and IGST rates will reduce the incentive for evasion 
 by eliminating rate arbitrage between neighbouring States and that between intra 
 and inter-state sales; 
 <li>Improved Compliance levels of the tax payers will
 contribute greatly in improving the revenue collection of the States.</li><br>
 </ul>
 
<h4>5.0 Current status</h4><br>
<ul>
<li>In order to implement this tax reform, Constitutional (122nd Amendment)     
Bill (CAB for short) was introduced in the Parliament and passed by Rajya 
Sabha on 03rd August, 2016 and  Lok Sabha on 08th August, 2016.</li>
<li>The CAB was passed by more than 15 states and thereafter Hon’ble President gave 
assent  to “The Constitution (One Hundred And First Amendment) Act, 2016” on 8th of 
September, 2016. Since then the GST council and been notified bringing into 
existence the Constitutional body to decide issues relating to GST. </li>
<li>On September 16, 2016, Government of India issued notifications bringing 
into effect all the sections of CAB setting firmly into motion the rolling 
out of GST. This notification sets out an outer limit of time of one year, 
that is till 15-9-2017 for bringing into effect GST.</li> 
<li>The Central Goods and Services Tax Bill, Integrated Goods and Services Tax Bill, 
Union Territories (without legislature) Goods and Services Tax Bill and 
Goods and Services Tax (Compensation to States) Bill have been passed by 
the Lok Sabha on 29.03.2017 and by the Rajya Sabha on 06.04.2017.These Acts 
have been notified on 12th April, 2017 after the assent of the President.</li><br>
</ul>
 

